import Foundation

// MARK: - For optional values closure based matching
// Usage:
// searchBar.text.matching { $0.count > 2 }.map(performSearch)
extension Optional {
    func matching(_ predicate: (Wrapped) -> Bool) -> Wrapped? {
        guard let value = self else {
            return nil
        }
        
        guard predicate(value) else {
            return nil
        }
        
        return value
    }
}
